public class Code03_09_new {
    public static void main(String [] args){
        int num=20;
        System.out.println(++num + " : num 변수 출력 전에 +1이 되어서 21이 출력됨 ");
        System.out.println(num++ + " : num이 출력되고 +1을 수행 ");
        System.out.println(num + " : 22 출력");
        System.out.println(--num + " : num 변수 출력 전에 -1이 되어서 21이 출력됨");
        System.out.println(num-- + " : num이 출력되고 -1을 수행");
        System.out.println(num + " : 20 출력");
    }
}
